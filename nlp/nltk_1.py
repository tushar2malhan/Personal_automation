
# what is stemming ?
# stemming is the process of reducing a word to its root form
# example: ['Organized','Organizes','organising] -> ['Organize']

# what is tokenaizing ?
# tokenaizing is the process of breaking a sentence into words
# example: 'I am a student and do i like maths ?' -> ['I','am','a','student','and','do','i','like','maths','?']

